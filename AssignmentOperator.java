public class AssignmentOperator {
    public static void main(String[] args){
        int x = 10;
        System.out.println(x);
    
        x+=5;
        System.out.println(x);
       
        x-=3;
        x*=2;
        x/=7;
        x%=8;
    
        System.out.println(x++);



        System.out.println(x);
    }
}